-- examples using select 

SELECT subject FROM posts  -- selecting only subject column from posts Table

SELECT * FROM posts  -- selecting everything from posts table

SELECT * FROM posts WHERE id='1' -- selecting everything from posts table where id=1

SELECT * FROM posts WHERE id='1' AND subject='This is the subject' -- AND

SELECT * FROM posts WHERE id='1' OR subject='This is the subject' -- OR
